function makeHanja([character, sound, meaning, hanja, word, wordMeaning], day, index, level) {
  const cycle = Math.floor((day - 1) / levelGroups[level].length) + 1;
  const role = index < 2 ? "서로 관계가 있는 중심 한자" : "같은 소리나 비슷한 소리를 통해 비교하는 확장 한자";
  const criteria = levelCriteria[level];
  return {
    character,
    sound,
    meaning,
    radical: character,
    relation: `${level} ${day}일차 ${index + 1}번 한자입니다. ${role}로 묶어 학습합니다.`,
    origin: `${character}은 '${meaning}'의 뜻을 지닌 한자로, 어휘 속에서 뜻이 어떻게 확장되는지 살펴봅니다.`,
    difficultyNote: criteria ? `${criteria.target} 기준: ${criteria.focus}` : "",
    vocab: makeVocabSet({ character, sound, meaning, hanja, word, wordMeaning, level }),
    reviewCycle: cycle
  };
}

function makeVocabSet({ character, sound, meaning, hanja, word, wordMeaning, level }) {
  const curated = curatedVocabByCharacter[character];
  const templates = curated ? curated.map(([itemHanja, itemWord, itemMeaning], index) => ({
    hanja: itemHanja,
    word: itemWord,
    meaning: itemMeaning,
    topic: index === 0 ? "대표 어휘" : "확장 어휘"
  })) : uniqueVocabTemplates([
    { hanja, word, meaning: wordMeaning, topic: "대표 어휘" },
    { hanja: `${character}力`, word: `${sound}력`, meaning: `${meaning}의 힘이나 능력`, topic: "능력 어휘" },
    { hanja: `${character}性`, word: `${sound}성`, meaning: `${meaning}와 관련된 성질`, topic: "성질 어휘" },
    { hanja: `${character}度`, word: `${sound}도`, meaning: `${meaning}의 정도나 수준`, topic: "정도 어휘" },
    { hanja: `${character}化`, word: `${sound}화`, meaning: `${meaning}와 관련된 상태로 바뀜`, topic: "변화 어휘" },
    { hanja: `${character}者`, word: `${sound}자`, meaning: `${meaning}와 관련된 사람`, topic: "사람 어휘" },
    { hanja: `${character}物`, word: `${sound}물`, meaning: `${meaning}와 관련된 물건이나 대상`, topic: "대상 어휘" },
    { hanja: `${character}心`, word: `${sound}심`, meaning: `${meaning}와 관련된 마음`, topic: "마음 어휘" },
    { hanja: `大${character}`, word: `대${sound}`, meaning: `크거나 중요한 ${meaning}의 상태`, topic: "확장 어휘" },
    { hanja: `自${character}`, word: `자${sound}`, meaning: `스스로 ${meaning}와 관련됨`, topic: level === "고급" ? "심화 어휘" : "확장 어휘" }
  ]).slice(0, 8);

  return templates.map((item) => ({
    hanja: item.hanja,
    word: item.word,
    meaning: item.meaning,
    examples: curatedExamplesByWord[item.word] || makeDictionaryLikeExamples(item.word, item.meaning)
  }));
}

function makeDictionaryLikeExamples(word, meaning) {
  const subjectParticle = particle(word, "이", "가");
  const objectParticle = particle(word, "을", "를");
  const topicParticle = particle(word, "은", "는");
  const connectParticle = particle(word, "과", "와");
  if (word.endsWith("력")) {
    return [
      `전문가는 ${word}${objectParticle} 키우기 위한 방법을 제안했다.`,
      `이번 계획에서는 ${word}${subjectParticle} 중요한 요소로 꼽혔다.`,
      `${word}${topicParticle} 꾸준한 노력으로 길러질 수 있다.`
    ];
  }
  if (word.endsWith("성")) {
    return [
      `이번 연구에서 ${word}${subjectParticle} 분명히 드러났다.`,
      `전문가들은 ${word}${objectParticle} 높이는 방법을 논의했다.`,
      `${word}${topicParticle} 결과를 판단하는 중요한 기준이 된다.`
    ];
  }
  if (word.endsWith("도")) {
    return [
      `기계의 ${word}${objectParticle} 일정하게 유지했다.`,
      `조사 결과 ${word}${subjectParticle} 이전보다 높아졌다.`,
      `전문가는 ${word}${objectParticle} 낮추기 위한 방법을 제안했다.`
    ];
  }
  if (word.endsWith("화")) {
    return [
      `도시의 ${word}${subjectParticle} 빠르게 진행되었다.`,
      `학교는 ${word}${objectParticle} 막기 위한 계획을 세웠다.`,
      `${word}${topicParticle} 생활 방식에도 영향을 주었다.`
    ];
  }
  if (word.endsWith("자")) {
    return [
      `${word}${subjectParticle} 회의에 참석했다.`,
      `담당자는 ${word}${objectParticle} 위해 안내문을 준비했다.`,
      `${word}${topicParticle} 자신의 의견을 차분히 밝혔다.`
    ];
  }
  if (word.endsWith("물")) {
    return [
      `창고에는 ${word}${subjectParticle} 가지런히 보관되어 있었다.`,
      `관리자는 ${word}${objectParticle} 조심스럽게 옮겼다.`,
      `${word}${topicParticle} 습기에 약해 따로 보관해야 한다.`
    ];
  }
  if (word.endsWith("심")) {
    return [
      `그 일 이후 아이에게 ${word}${subjectParticle} 생겼다.`,
      `선생님은 학생들의 ${word}${objectParticle} 북돋아 주었다.`,
      `${word}${topicParticle} 어려운 일을 계속하게 하는 힘이 된다.`
    ];
  }
  return [
    `최근 ${word}에 대한 관심이 높아졌다.`,
    `신문 기사에서는 ${word} 문제를 자세히 다루었다.`,
    `지역 사회는 ${word}${connectParticle} 관련한 대책을 마련했다.`
  ];
}

function particle(word, withBatchim, withoutBatchim) {
  const last = String(word || "").charCodeAt(String(word || "").length - 1);
  if (last < 0xac00 || last > 0xd7a3) return withoutBatchim;
  return (last - 0xac00) % 28 === 0 ? withoutBatchim : withBatchim;
}

function uniqueVocabTemplates(items) {
  const seen = new Set();
  return items.filter((item) => {
    const key = `${item.hanja}:${item.word}`;
    if (seen.has(key)) return false;
    seen.add(key);
    return item.hanja.length >= 2 && item.hanja.length <= 3 && item.word.length >= 2 && item.word.length <= 3;
  });
}

export function buildSeedCurriculum() {
  return Object.entries(levelGroups).flatMap(([level, days]) =>
    Array.from({ length: targetDaysPerLevel }, (_, index) => {
      const source = days[index % days.length];
      return {
        day: index + 1,
        grade: "공통",
        level,
        criteria: levelCriteria[level],
        dailyCount: 4,
        hanjaSet: source.map((item, itemIndex) => makeHanja(item, index + 1, itemIndex, level))
      };
    })
  );
}

